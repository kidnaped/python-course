# 2D lists = a list of lists

drinks = ["coffee", "tea", "juice", "cocktail"]
dinner = ["kotleta", "pasta", "sushi"]
dessert = ["ice cream", "cake", "chocolate"]

food = [drinks, dinner, dessert]

print(food[2][2])           #when working with 2d list in [] we choose an item of 2d list 
                            #and in second [] we choose item from chosen list